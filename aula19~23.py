# Operadores de comparação
# Operadores de comparação são usados para comparar valores e retornar um booleano (True ou False).

# #OP      Significado         Exemplo (True)
# >       maior               2 > 1
# >=      maior ou igual      2 >= 2
# <       menor               1 < 2
# <=      menor ou igual      2 <= 2
# ==      igual               'a' == 'a'
# !=      diferente           'a' != 'b'

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'
print('maior:', maior)
print('maior_ou_igual:', maior_ou_igual)
print('menor:', menor)
print('menor_ou_igual:', menor_ou_igual)
print('igual:', igual)
print('diferente:', diferente)

# Operadores Lógicos
# Operadores lógicos são usados para combinar expressões booleanas.

# #OP      Significado         Exemplo (True)
# and     e                   True and True
# or      ou                  True or False
# not     não                 not True
# in      está em             'a' in 'abc'
# not in  não está em         'd' not in 'abc'

e = True and True
ou = True or False
nao = not True
esta_em = 'a' in 'abc'
nao_esta_em = 'd' not in 'abc'

print('e:', e)
print('ou:', ou)
print('não:', nao)
print('está em:', esta_em)
print('não está em:', nao_esta_em)


